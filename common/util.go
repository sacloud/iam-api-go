package common

// generic-ish type cast helper function
func IntoOpt[T, U any, P interface {
	*T
	Reset()
	SetTo(u U)
}](v *U) T {
	var opt T
	if v == nil {
		P(&opt).Reset()
	} else {
		P(&opt).SetTo(*v)
	}
	return opt
}

// generic-ish type cast helper function
func IntoNullable[T, U any, P interface {
	*T
	SetToNull()
	SetTo(u U)
}](v *U) T {
	var nullable T
	if v == nil {
		P(&nullable).SetToNull()
	} else {
		P(&nullable).SetTo(*v)
	}
	return nullable
}
